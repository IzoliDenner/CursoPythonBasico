"""
str - string
"""

# strings são textos dentro de aspas ""

print('alguma coisa')
print("aspas duplas")
print(123456)

print("esta é uma 'string' (str).")  # se quero ter um texto com aspas aparecendo, posso utilizar aspas diferentes para isso
print('esta é uma "string" (str).')  # preferivel utilizar esta maneira
print("Esse é meu \"texto\" (str).")  # também posso utilizar \ barra invertida antes do testo e antes das aspas

print('Esse é meu \n (str). ')  # barra n - \n = quebra linha

print(r'Esse é meu \n (str). ')  # r no inicio, não deixa nada dentro das aspas serem executadas
